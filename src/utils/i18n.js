/**
 * Translates a key based on active language and processes variables.
 */
export const t = (key, lang = 'en', variables = {}) => {
  const langTranslations = translations[lang] || translations.en;
  let text = langTranslations[key] || translations.en[key] || key;
  
  Object.keys(variables).forEach(varKey => {
    text = text.replaceAll(`{${varKey}}`, variables[varKey]);
  });
  
  return text;
};
